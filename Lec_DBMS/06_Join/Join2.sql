SELECT c.C_NAME 고객명,c.C_POINT POINT, g.G_NAME 상품명
FROM T_GIFT g,T_CUSTOMER c
WHERE c.C_POINT BETWEEN g.G_START AND G_END ;

SELECT c.C_NAME 고객명,c.C_POINT POINT, g.G_NAME 상품명
FROM T_GIFT g join T_CUSTOMER c
on c.C_POINT BETWEEN g.G_START AND G_END

--#6202
SELECT g.G_NAME 상품명 ,COUNT(g.G_NAME) 필요수량
FROM T_GIFT g join T_CUSTOMER c
on c.C_POINT BETWEEN g.G_START AND G_END
GROUP BY g.G_NAME;


