SELECT p1.DEPTNO 교수번호,p1.NAME 교수명 ,p1.HIREDATE 입사일 ,count(p2.HIREDATE) 빠른사람
FROM T_PROFESSOR p1 LEFT OUTER join T_PROFESSOR p2
ON p1.HIREDATE >p2.HIREDATE
GROUP BY 
p1.DEPTNO,p1.NAME ,p1.HIREDATE 
ORDER BY count(p2.HIREDATE) ASC
;

